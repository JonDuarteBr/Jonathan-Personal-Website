
function MoreAndLess1() {
	if (document.getElementById("dropdown1").innerHTML == "more") {
	document.getElementById("dropdown1").innerHTML = "less";
	document.getElementById("dropdowninfo1").style.display = "inline";
	} else if (document.getElementById("dropdown1").innerHTML == "less") {
	document.getElementById("dropdown1").innerHTML = "more";
	document.getElementById("dropdowninfo1").style.display = "none";
	}
}

